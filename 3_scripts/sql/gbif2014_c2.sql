GBIF2014_C1.FECHA, 
GBIF2014_C1.TAXON, 
GBIF2014_C1.COD_TAXON, 
GBIF2014_C1.COTA, 
GBIF2014_C1.ID_PARCELA, 
GBIF2014_C1.ID_VISITA, 
GBIF2014_C1.ID_INVENTARIO, 
GBIF2014_C1.NUM_ESTRCT_FLORALES, 
GBIF2014_C1.NUM_FRUTOS, 
